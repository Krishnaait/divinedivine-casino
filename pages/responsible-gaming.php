                    <li><strong>Permanent Closure:</strong> Permanently close your account</li>
                    <li><strong>Cool-Off Period:</strong> Take a mandatory break before reactivating</li>
                </ul>
                <p>To request self-exclusion, contact <strong>support@dinedivine.com</strong></p>

                <h3>5.3 Account Controls</h3>
                <ul>
                    <li>Balance reset limits to prevent excessive virtual currency accumulation</li>
                    <li>Betting limit controls (minimum and maximum bets)</li>
                    <li>Activity monitoring and reporting</li>
                </ul>
            </div>

            <div class="legal-section">
                <h2>6. Tips for Responsible Gaming</h2>
                <h3>6.1 Set Boundaries</h3>
                <ul>
                    <li>Decide in advance how much time you'll spend gaming</li>
                    <li>Set a schedule and stick to it</li>
                    <li>Use alarms or timers to track gaming time</li>
                    <li>Avoid gaming during work or study hours</li>
                </ul>

                <h3>6.2 Balance Your Life</h3>
                <ul>
                    <li>Maintain other hobbies and interests</li>
                    <li>Spend quality time with family and friends</li>
                    <li>Exercise regularly and maintain physical health</li>
                    <li>Get adequate sleep (7-9 hours per night)</li>
                    <li>Fulfill work, school, and personal responsibilities first</li>
                </ul>

                <h3>6.3 Stay Informed</h3>
                <ul>
                    <li>Understand that gaming outcomes are based on chance</li>
                    <li>Remember that virtual currency has no real-world value</li>
                    <li>Don't chase losses or try to "win back" virtual currency</li>
                    <li>Recognize that gaming is entertainment, not a way to make money</li>
                </ul>

                <h3>6.4 Take Regular Breaks</h3>
                <ul>
                    <li>Take a 10-15 minute break every hour</li>
                    <li>Stand up, stretch, and move around</li>
                    <li>Rest your eyes by looking away from the screen</li>
                    <li>Stay hydrated and eat regular meals</li>
                </ul>

                <h3>6.5 Monitor Your Emotions</h3>
                <ul>
                    <li>Don't game when feeling stressed, anxious, or depressed</li>
                    <li>Avoid using gaming as an escape from problems</li>
                    <li>Stop gaming if you feel frustrated or angry</li>
                    <li>Seek healthier coping mechanisms for negative emotions</li>
                </ul>
