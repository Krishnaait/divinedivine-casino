                <h2>4. Signs of Problem Gaming</h2>
                <p>Gaming may be becoming problematic if you experience any of the following:</p>
                
                <h3>4.1 Behavioral Signs</h3>
                <ul>
                    <li>Spending more time gaming than intended</li>
                    <li>Neglecting work, school, or family responsibilities</li>
                    <li>Gaming to escape problems or relieve negative feelings</li>
                    <li>Lying to others about the amount of time spent gaming</li>
                    <li>Feeling restless or irritable when unable to game</li>
                    <li>Repeatedly unsuccessful attempts to control or stop gaming</li>
                </ul>

                <h3>4.2 Social and Emotional Signs</h3>
                <ul>
                    <li>Losing interest in other hobbies and activities</li>
                    <li>Experiencing relationship problems due to gaming</li>
                    <li>Feeling anxious, depressed, or guilty about gaming</li>
                    <li>Isolating yourself from friends and family</li>
                    <li>Gaming interfering with sleep patterns</li>
                </ul>

                <h3>4.3 Physical Signs</h3>
                <ul>
                    <li>Eye strain or headaches from extended screen time</li>
                    <li>Poor personal hygiene due to excessive gaming</li>
                    <li>Neglecting meals or proper nutrition</li>
                    <li>Lack of physical exercise</li>
                    <li>Fatigue from irregular sleep schedules</li>
                </ul>
            </div>

