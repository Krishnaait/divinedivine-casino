                <ul>
                    <li><strong>Session Timers:</strong> Set reminders for gaming session lengths</li>
                    <li><strong>Reality Checks:</strong> Periodic notifications about time spent gaming</li>
                    <li><strong>Daily Limits:</strong> Set maximum daily gaming time</li>
                    <li><strong>Break Reminders:</strong> Automatic prompts to take breaks</li>
                </ul>

                <h3>5.2 Self-Exclusion</h3>
                <p>If you feel you need a break from gaming, you can:</p>
                <ul>
